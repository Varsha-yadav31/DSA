//Given a number n, check whether it is even or odd. Return true for even and false for odd.
#include <iostream>
using namespace std;
int main(){
    int n ;
    cin>>n;
    if(n%2==0){
        cout << "even" <<endl;
        cout << "true"<<endl;
    }else{
        cout << "odd"<<endl;
        cout<<"false"<<endl;
        
    }
}
